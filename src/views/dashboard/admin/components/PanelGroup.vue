<template>
  <el-row :gutter="40" class="panel-group">
    <el-col :xs="12" :sm="12" :lg="8" class="card-panel-col">
      <div class="card-panel">
        <div class="card-panel-icon-wrapper icon-people">
          <svg-icon icon-class="peoples" class-name="card-panel-icon" />
        </div>
        <div class="card-panel-description">
          <div class="card-panel-text">
            用户访问量
          </div>
          <count-to :start-val="0" :end-val="visiters" :duration="2600" class="card-panel-num" />
        </div>
      </div>
    </el-col>
    <el-col :xs="12" :sm="12" :lg="8" class="card-panel-col">
      <div class="card-panel">
        <div class="card-panel-icon-wrapper icon-money">
          <svg-icon icon-class="star" class-name="card-panel-icon" />
        </div>
        <div class="card-panel-description">
          <div class="card-panel-text">
            题库总数
          </div>
          <count-to :start-val="0" :end-val="datas" :duration="3200" class="card-panel-num" />
        </div>
      </div>
    </el-col>
    <el-col :xs="12" :sm="12" :lg="8" class="card-panel-col">
      <div class="card-panel">
        <div class="card-panel-icon-wrapper icon-shopping">
          <svg-icon icon-class="search" class-name="card-panel-icon" />
        </div>
        <div class="card-panel-description">
          <div class="card-panel-text">
            查询次数
          </div>
          <count-to :start-val="0" :end-val="search" :duration="3600" class="card-panel-num" />
        </div>
      </div>
    </el-col>
  </el-row>
</template>

<script>
import CountTo from 'vue-count-to';
import { getCount } from '@/api/system/firstpage';

export default {
  components: {
    CountTo
  },
  data() {
    return {
      // 用户访问数量即登录次数，数据库表为sys_login_info
      visiters: 0,
      // 短信发送总数 数据库表为sys_sms_log，此数据库可删除
      // messages: 0,
      // 总数据条数 数据库表为garbage_store
      datas: 0,
      // 不同种类垃圾搜索次数 数据库表为keyword_result
      search: 0
    }
  },
  created() {
    this.getAllCount();
  },
  methods: {
    getAllCount() {
      getCount().then(res => {
        this.visiters = res.data[0];
        // this.messages = res.data[1]
        this.datas = res.data[2];
        this.search = res.data[3];
      })
    },
  }
}
</script>

